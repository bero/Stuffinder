const translations: Record<Locale, Dict> = { en, sv, fi, de, fr, it, es, pt };

const LOCALE_KEY = 'stuffinder:locale';
let listeners: Array<() => void> = [];

function readLocale(): Locale {
  const stored = (typeof localStorage !== 'undefined' ? localStorage.getItem(LOCALE_KEY) : null) as Locale | null;
  if (stored && translations[stored]) return stored;
  return 'en';
}

export function setLocale(l: Locale) {
  localStorage.setItem(LOCALE_KEY, l);
  listeners.forEach(fn => fn());
}

export function useLocale(): Locale {
  const [locale, setLocaleState] = useState<Locale>(readLocale());
  useEffect(() => {
    const handler = () => setLocaleState(readLocale());
    listeners.push(handler);
    return () => { listeners = listeners.filter(f => f !== handler); };
  }, []);
  return locale;
}

function format(str: string, vars?: Record<string, string | number>): string {
  if (!vars) return str;
  let out = str;
  for (const [k, v] of Object.entries(vars)) {
    out = out.split(`{${k}}`).join(String(v));
  }
  return out;
}

export function useT(): (key: string, vars?: Record<string, string | number>) => string {
  const locale = useLocale();
  return (key, vars) => {
    const dict = translations[locale];
    const str = (dict && dict[key]) || translations.en[key] || key;
    return format(str, vars);
  };
}

// Non-hook variant for use in event handlers / confirm() / alert()
export function t(key: string, vars?: Record<string, string | number>): string {
  const locale = readLocale();
  const dict = translations[locale];
  const str = (dict && dict[key]) || translations.en[key] || key;
  return format(str, vars);
}

export function formatDate(date: string | Date): string {
  const locale = readLocale();
  const tag = locale === 'en' ? 'en-GB' : locale;
  try {
    return new Intl.DateTimeFormat(tag).format(new Date(date));
  } catch {
    return new Date(date).toLocaleDateString();
  }
}
